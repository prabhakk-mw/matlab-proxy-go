func TestClearStaleCookieMiddleware(t *testing.T) {
	ta := newTestAuth(t, true, "new-token", 8080)
	handler := ta.ClearStaleCookieMiddleware(http.HandlerFunc(func(w http.ResponseWriter, r *http.Request) {
		w.WriteHeader(http.StatusOK)
	}))

	// Request with a stale cookie (from a previous session)
	req := httptest.NewRequest("GET", "/", nil)
	req.AddCookie(&http.Cookie{Name: "mwi-auth-session-8080", Value: "old-session-hash"})
	w := httptest.NewRecorder()
	handler.ServeHTTP(w, req)

	// Should expire the stale cookie
	cookies := w.Result().Cookies()
	found := false
	for _, c := range cookies {
		if c.Name == "mwi-auth-session-8080" {
			found = true
			if c.MaxAge != -1 {
				t.Errorf("expected MaxAge=-1 to expire cookie, got %d", c.MaxAge)
			}
		}
	}
	if !found {
		t.Error("expected stale cookie to be expired")
	}
}

func TestClearStaleCookieMiddleware_ValidCookie(t *testing.T) {
	ta := newTestAuth(t, true, "current-token", 8080)
	handler := ta.ClearStaleCookieMiddleware(http.HandlerFunc(func(w http.ResponseWriter, r *http.Request) {
		w.WriteHeader(http.StatusOK)
	}))

	// Request with a valid cookie
	req := httptest.NewRequest("GET", "/", nil)
	req.AddCookie(&http.Cookie{Name: "mwi-auth-session-8080", Value: ta.TokenHash()})
	w := httptest.NewRecorder()
	handler.ServeHTTP(w, req)

	// Should NOT expire a valid cookie
	for _, c := range w.Result().Cookies() {
		if c.Name == "mwi-auth-session-8080" && c.MaxAge == -1 {
			t.Error("valid cookie should not be expired")
		}
	}
}
