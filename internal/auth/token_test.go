func newTestAuth(t *testing.T, enabled bool, token string, port int) *TokenAuth {
	t.Helper()
	cfg := &config.Config{
		EnableTokenAuth: enabled,
		AuthToken:       token,
		Port:            port,
	}
	ta, err := New(cfg)
	if err != nil {
		t.Fatalf("New() error: %v", err)
	}
	return ta
}

func TestNew_Disabled(t *testing.T) {
	ta := newTestAuth(t, false, "", 8080)
	if ta.Enabled() {
		t.Error("expected Enabled() = false")
	}
	if ta.Token() != "" {
		t.Error("expected empty token when disabled")
	}
}

func TestNew_AutoGeneratedToken(t *testing.T) {
	ta := newTestAuth(t, true, "", 8080)
	if !ta.Enabled() {
		t.Error("expected Enabled() = true")
	}
	if ta.Token() == "" {
		t.Error("expected auto-generated token")
	}
	if len(ta.Token()) != 64 { // 32 bytes hex-encoded
		t.Errorf("expected 64-char token, got %d", len(ta.Token()))
	}
}

func TestNew_CustomToken(t *testing.T) {
	ta := newTestAuth(t, true, "my-secret", 8080)
	if ta.Token() != "my-secret" {
		t.Errorf("expected token 'my-secret', got %q", ta.Token())
	}
}

func TestTokenHash(t *testing.T) {
	ta := newTestAuth(t, true, "test-token", 8080)
	h := sha256.Sum256([]byte("test-token"))
	expected := hex.EncodeToString(h[:])
	if ta.TokenHash() != expected {
		t.Errorf("expected hash %q, got %q", expected, ta.TokenHash())
	}
}

func TestCookieName_PortScoped(t *testing.T) {
	ta1 := newTestAuth(t, true, "tok", 8080)
	ta2 := newTestAuth(t, true, "tok", 9090)
	if ta1.cookieName == ta2.cookieName {
		t.Errorf("cookie names should differ for different ports, both got %q", ta1.cookieName)
	}
	if ta1.cookieName != "mwi-auth-session-8080" {
		t.Errorf("expected 'mwi-auth-session-8080', got %q", ta1.cookieName)
	}
}

