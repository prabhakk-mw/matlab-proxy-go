func TestSetSessionCookie(t *testing.T) {
	ta := newTestAuth(t, true, "secret123", 8080)
	w := httptest.NewRecorder()
	ta.SetSessionCookie(w)

	cookies := w.Result().Cookies()
	if len(cookies) != 1 {
		t.Fatalf("expected 1 cookie, got %d", len(cookies))
	}
	c := cookies[0]
	if c.Name != "mwi-auth-session-8080" {
		t.Errorf("expected cookie name 'mwi-auth-session-8080', got %q", c.Name)
	}
	if c.Value != ta.TokenHash() {
		t.Errorf("expected cookie value %q, got %q", ta.TokenHash(), c.Value)
	}
	if !c.HttpOnly {
		t.Error("cookie should be HttpOnly")
	}
}

